#ifndef EFI_FILESYSTEM_H
#define EFI_FILESYSTEM_H

#include <efi/sys_table.h>
#include <efi/types.h>
#include <efi/protocols/media_access.h>

#define EFI_FILE_MODE_READ       0x0000000000000001
#define EFI_FILE_MODE_WRITE      0x0000000000000002
#define EFI_FILE_MODE_CREATE     0x8000000000000000

    
#define EFI_FILE_READ_ONLY      0x0000000000000001
#define EFI_FILE_HIDDEN         0x0000000000000002
#define EFI_FILE_SYSTEM         0x0000000000000004
#define EFI_FILE_RESERVED       0x0000000000000008
#define EFI_FILE_DIRECTORY      0x0000000000000010
#define EFI_FILE_ARCHIVE        0x0000000000000020
#define EFI_FILE_VALID_ATTR     0x0000000000000037

typedef struct
{
    EFI_STATUS status;
    EFI_FILE_PROTOCOL *file;
    EFI_FILE_INFO info;
} file_t;

EFI_STATUS get_root(EFI_FILE_PROTOCOL **root);
EFI_STATUS get_loader_image(EFI_HANDLE loader, EFI_SYSTEM_TABLE *system, EFI_LOADED_IMAGE_PROTOCOL **image);

file_t sfs_open(CHAR16 *path, UINT64 mode);
EFI_STATUS sfs_read(file_t *file, void *buffer);
EFI_STATUS sfs_close(file_t *file);

#endif
